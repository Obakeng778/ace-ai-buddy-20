import { createLovableAiGatewayProvider } from "@/lib/ai-gateway.server";
import { createFileRoute } from "@tanstack/react-router";
import { convertToModelMessages, streamText, type UIMessage } from "ai";

const SYSTEM_PROMPT = `You are the AI assistant inside "Tech for you by OB", a workplace productivity suite.
- Be concise, warm, and professional. Prefer structured answers with clear headings and bullet lists when helpful.
- Support markdown formatting (headings, lists, bold, code blocks).
- When asked to draft emails, meeting notes, or plans, produce ready-to-use output the user can copy immediately.
- Never claim to have real-time data. Ask for missing context before assuming.
- Remind users (only once per conversation, briefly) not to share confidential or personal data if they appear to be doing so.`;

export const Route = createFileRoute("/api/chat")({
  server: {
    handlers: {
      POST: async ({ request }) => {
        const { messages } = (await request.json()) as { messages?: unknown };
        if (!Array.isArray(messages)) {
          return new Response("Messages are required", { status: 400 });
        }
        const key = process.env.LOVABLE_API_KEY;
        if (!key) return new Response("Missing LOVABLE_API_KEY", { status: 500 });

        const gateway = createLovableAiGatewayProvider(key);
        const result = streamText({
          model: gateway("google/gemini-3-flash-preview"),
          system: SYSTEM_PROMPT,
          messages: await convertToModelMessages(messages as UIMessage[]),
        });

        return result.toUIMessageStreamResponse({
          originalMessages: messages as UIMessage[],
        });
      },
    },
  },
});
